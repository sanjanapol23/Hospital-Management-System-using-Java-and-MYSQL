    private void txtdcodeKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtdcodeKeyPressed
        // TODO add your handling code here:
        
        if(evt.getKeyCode() == KeyEvent.VK_ENTER)
        {
            String dcode = txtdcode.getText();
            
            try {
                pst = (PreparedStatement) con.prepareStatement("select * from item where itemid = ?");
                pst.setString(1,dcode);
                rs = pst.executeQuery();
                
                if(rs.next() == false)
                {
                    JOptionPane.showMessageDialog(this, "Drug Not Found");
                }
                
                else
                {
                    String dname = rs.getString("itemname");
                    txtdname.setText(dname.trim());
                    txtqty.requestFocus();
                }
                
                
            } catch (SQLException ex) {
                Logger.getLogger(Inventory.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
        
        
    }//GEN-LAST:event_txtdcodeKeyPressed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String dcode = txtdcode.getText();
        try {
            pst = (PreparedStatement) con.prepareStatement("select * from item where itemid = ?");
            pst.setString(1,dcode);
            rs = pst.executeQuery();
            
            
            while(rs.next())
            {
                int currentqty;
                int sellprice;
                int qty;
                
                currentqty = rs.getInt("qty");
                sellprice = rs.getInt("sellprice");
                
                qty = Integer.parseInt(txtqty.getValue().toString());
                
                int total = sellprice * qty; 

                
                
                if(qty >= currentqty)
                {
                    JOptionPane.showMessageDialog(this, "Avaliable Item" + currentqty);
                    JOptionPane.showMessageDialog(this, "Quantity Not Enough");

                }
                
                else
                {
                    DefaultTableModel df = (DefaultTableModel) jTable1.getModel();
                    df.addRow(new Object[]
                    {
                        lbpid.getText(),
                        txtdcode.getText(),
                        txtdname.getText(),
                        txtqty.getValue().toString(),
                        sellprice,
                        total,
                    });
                    
                    int sum = 0;
                    
                    for(int i = 0;i < jTable1.getRowCount(); i++)
                    {
                        sum = sum + Integer.parseInt(jTable1.getValueAt(i, 5).toString());
                    }
                    
                    txttotal.setText(Integer.toString(sum));
                    txtdcode.setText("");
                    txtdname.setText("");
                    txtqty.setValue(0);
                    txtdcode.requestFocus();
                }
                
            }
            
            
        } catch (SQLException ex) {
            Logger.getLogger(Inventory.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

