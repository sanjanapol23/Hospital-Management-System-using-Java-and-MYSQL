    public void sales()
    {
        DateTimeFormatter des = DateTimeFormatter.ofPattern("yyyy/MM/dd");
        LocalDateTime now = LocalDateTime.now();
        String date = des.format(now);
        
        String subtotal = txttotal.getText();
        String pay = txtpay.getText();
        String balance = txtbalance.getText();
        
        int lastinsertid = 0;
        
        try {
            String query = "insert into sales(date,subtotal,pay,balance) values(?,?,?,?)";
            pst = (PreparedStatement) con.prepareStatement(query,Statement.RETURN_GENERATED_KEYS);
            pst.setString(1, date);
            pst.setString(2, subtotal);
            pst.setString(3, pay);
            pst.setString(4, balance);
            
            pst.executeUpdate();
            
            
            rs = pst.getGeneratedKeys();
            
            if(rs.next())
            {
                lastinsertid = rs.getInt(1);
            }
            
            int rows = jTable1.getColumnCount();
            
            String query1 = "insert into saleproduct(sales_id,product_id,sellprice,qty,total) values(?,?,?,?,?)";
            pst = (PreparedStatement) con.prepareStatement(query1);
            
            String pres_id;
            String item_id;
            String item_name;
            String price;
            String qty;
            int total;
            
            
            for(int i=0;i<jTable1.getRowCount();i++)
            {
                pres_id = (String)jTable1.getValueAt(i, 0);
                item_id = (String)jTable1.getValueAt(i, 1);
                qty = jTable1.getValueAt(i, 3).toString();
                int qty1 = Integer.parseInt(qty);
                price = jTable1.getValueAt(i, 4).toString();
                int price1 = Integer.parseInt(price);
                total = (int)jTable1.getValueAt(i, 5);
                
                
                pst.setInt(1, lastinsertid);
                pst.setString(2, item_id); 
                pst.setInt(3, qty1);
                pst.setInt(4, price1);
                pst.setInt(5,total);
                
                pst.executeUpdate();
                
                JOptionPane.showMessageDialog(this, "Recorded Saved");
            }
            
            
        } catch (SQLException ex) {
            Logger.getLogger(Inventory.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
